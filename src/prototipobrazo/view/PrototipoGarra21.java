    public void accion(){
        switch(cmbEstante.getSelectedItem().toString()){
            case "1":
                if(cmbPiso.getSelectedItem().toString().equals("1")){
                    conexion.escribir(1);
                }else{
                    conexion.escribir(2);
                }
                break;
            case "4": 
                if(cmbPiso.getSelectedItem().toString().equals("1")){
                    conexion.escribir(3);
                }else{
                    conexion.escribir(4);
                }
                break;
        }
    }
